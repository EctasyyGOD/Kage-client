    public static void drawCircle(double x, double y, float radius, int color) {
        final float f = ((color >> 24) & 0xff) / 255F;
        final float f1 = ((color >> 16) & 0xff) / 255F;
        final float f2 = ((color >> 8) & 0xff) / 255F;
        final float f3 = (color & 0xff) / 255F;
        GL11.glEnable(GL11.GL_BLEND);
        GL11.glDisable(GL11.GL_TEXTURE_2D);
        GL11.glEnable(GL11.GL_LINE_SMOOTH);
        GL11.glBlendFunc(GL11.GL_SRC_ALPHA, GL11.GL_ONE_MINUS_SRC_ALPHA);
        GL11.glColor4f(f1, f2, f3, f);
        GL11.glBegin(GL11.GL_TRIANGLE_FAN);

        for (int i = 0; i <= 360; i++) {
            final double x2 = Math.sin(((i * Math.PI) / 180)) * radius;
            final double y2 = Math.cos(((i * Math.PI) / 180)) * radius;
            GL11.glVertex2d(x + x2, y + y2);
        }

        GL11.glEnd();
        GL11.glDisable(GL11.GL_LINE_SMOOTH);
        GL11.glEnable(GL11.GL_TEXTURE_2D);
        GL11.glDisable(GL11.GL_BLEND);
    }

